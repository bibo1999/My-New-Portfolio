/**
 nav bar disapear while scroll and appear when I go up
 */
let lastScrollTop = 0; // Keep track of the last scroll position
let navbar = document.getElementById('navigation-bar'); // Get the navigation bar

// Add an event listener to the window to track scroll events
window.addEventListener('scroll', function() {
    let currentScroll = window.pageYOffset || document.documentElement.scrollTop; // Get the current scroll position

    if (currentScroll > lastScrollTop) {
        // If scrolling down, hide the navbar
        navbar.style.top = "-145px"; // Adjust this value to match the height of your navbar
    } else {
        // If scrolling up, show the navbar
        navbar.style.top = "0";
    }

    lastScrollTop = currentScroll <= 0 ? 0 : currentScroll; // Prevent negative scroll positions
});





//tabs
document.querySelectorAll('.tab-button').forEach(button => {
  button.addEventListener('click', () => {
    const tabList = button.parentElement;
    const tabHighlight = tabList.querySelector('.tab-highlight');
    const tabPanels = tabList.nextElementSibling;

    // Set active button
    tabList.querySelectorAll('.tab-button').forEach(btn => {
      btn.setAttribute('aria-selected', 'false');
    });
    button.setAttribute('aria-selected', 'true');

    // Move the highlight
    tabHighlight.style.left = button.offsetLeft + 'px';
    tabHighlight.style.width = button.offsetWidth + 'px';

    // Show the active panel
    tabPanels.querySelectorAll('.tab-panel').forEach(panel => {
      panel.setAttribute('aria-hidden', 'true');
      panel.classList.add('hidden');
    });
    const activePanel = tabPanels.querySelector(`#${button.getAttribute('aria-controls')}`);
    activePanel.setAttribute('aria-hidden', 'false');
    activePanel.classList.remove('hidden');
  });
});

// Set initial highlight position
window.onload = function() {
  const activeTab = document.querySelector('.tab-button[aria-selected="true"]');
  const tabHighlight = document.querySelector('.tab-highlight');
  tabHighlight.style.left = activeTab.offsetLeft + 'px';
  tabHighlight.style.width = activeTab.offsetWidth + 'px';
};
